public class PTra01_04 {
	public static void main(String[] args) {

		// ★ float型の変数 f を宣言してください
		float f;

		// ★ 変数 f に 3.14 を代入してください
		/**解答例は　f = 3.14F;　後ろのFはfloat型の定数のためのサフィックス (接尾辞) 
		 * double型の式中やdouble型への代入 (これも式ですが) で使うのは意味がないが，
float型の式中やfloat型への代入では意味を持つ。**/
		f = (float)3.14;

		// ★ 変数 f を出力してください
		System.out.println(f);

	}
}
